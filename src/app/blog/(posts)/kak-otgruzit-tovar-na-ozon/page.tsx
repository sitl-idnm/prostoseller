import { BlogPost } from '@/modules/blogPost'
import { getPosts } from '@/app/blog/posts'
import type { Metadata } from 'next'
// eslint-disable-next-line @typescript-eslint/consistent-type-imports
import meta from './meta.json'

export const metadata: Metadata = {
	title: meta.title,
	description: meta.description ?? ''
}

export default async function PostPage() {
	const currentId = 'kak-otgruzit-tovar-na-ozon'
	const posts = await getPosts()
	const related = posts
		.filter(p => p.id !== currentId)
		.map(p => ({
			id: p.id,
			title: p.title,
			href: `/blog/${p.id}`,
			description: p.description,
			imageSrc: p.imageSrc,
			date: p.date,
			tags: p.tags
		}))

	return (
		<BlogPost
			title="Как отгрузить товар на Озон"
			author="Александр Петров"
			authorRole="Учредитель компании"
			date="18.09.2024"
			readTimeMin={5}
			views={2165}
			imageSrc="/images/banner.jpg"
			currentId={currentId}
			related={related}
		>
			<p>Подписывая договор с площадкой Ozon, важно в дальнейшем соблюдать все его условия, в том числе каждую деталь процесса доставки. Иначе товары на склад или в пункт выдачи просто не примут. Продавец потеряет время и деньги, придётся организовывать поставку повторно. Далее расскажем о том, как сделать всё правильно с учётом выбранной схемы сотрудничества.</p>

			<h2>Как работает логистика на Ozon</h2>
			<p>Интернет-магазин Озон дорожит своей репутацией. Во избежание разочарования покупателей товары не отображаются в продаже до тех пор, пока они не поступили на склад.</p>

			<p>Для продавца это означает, что он не может просто заполнить карточку и ждать заявок. Нужно подтвердить, что товар существует и имеется в наличии. Ему нужно либо сделать отгрузку на склад маркетплейса, либо доказать, что партия в указанном количестве есть на его собственном складе — в зависимости от схемы работы.</p>

			<p>В дальнейшем алгоритмы сайта отслеживают остатки. Как только товар закончится, он будет недоступен для заказа — до тех пор, пока селлер вновь не проведёт отгрузку или не подтвердит новый привоз.</p>

			<h2>Что нужно сделать до поставки</h2>
			<p>Карточки товаров лучше создать заранее. Так будет удобнее оформлять заявку: не придётся переходить из раздела в раздел, выполнять дважды одну и ту же работу.</p>

			<p>Ещё до оформления заявок на поставку нужно точно узнать, подлежит ли выбранный товар сертификации. Если да, то маркетплейс обязательно затребует соответствующий документ при поставке. И, конечно же, откажет, если его не будет, а вдобавок заблокирует карточки до загрузки документов.</p>

			<h2>Алгоритм поставки товара на Ozon</h2>
			<p>Подготовка к поставке товара новичкам может показаться долгой и сложной. В первые несколько раз, действительно, может быть непросто, но, выучив на практике процесс отправки, селлеры легко справляются с ним.</p>

			<p>Поставка происходит пошагово. Во многих случаях действия нельзя менять местами.</p>

			<h3>Создание заказа на поставку</h3>
			<p>Прежде всех остальных действий нужно создать заявку на поставку:</p>
			<ul>
				<li>Зайти в личный кабинет на маркетплейсе.</li>
				<li>Если для товаров, которые планируется поставить на склад, ещё не были созданы карточки, нужно их создать.</li>
				<li>Перейти в раздел продаж.</li>
				<li>Выбрать пункт с заявками на поставку.</li>
				<li>Отметить галочками те товары, которые будут привезены на склад в ближайшее время, указав количество единиц для каждого из них. Этот момент критичен: если будут ошибки, маркетплейс отклонит поставку.</li>
			</ul>

			<h3>Выбор типа поставки</h3>
			<p>Прежде чем продавец будет оформлять заявку на поставку товара, ему нужно определиться со схемой сотрудничества, если он этого ещё не сделал. От выбранного формата зависит, как именно будет проходить весь процесс.</p>

			<p>Доступных схем у Озона три:</p>
			<ul>
				<li>FBO.</li>
				<li>FBS.</li>
				<li>realFBS.</li>
			</ul>

			<p>Выбор не ограничивается общими условиями работы. Есть правила и ограничения, которые рекомендуется подробно изучить. Например, некоторые товары Озон не доставляет, и тогда подходит только realFBS. В ряде случаев продавцам невыгодно отвозить небольшие заказы на склад, гораздо удобнее работать по FBO. И так далее.</p>

			<h4>Модель FBS</h4>
			<p>Селлер хранит продукцию на собственном складе. Только после заказа он доставляет товары в сортировочные центры маркетплейса, и тогда их отвозят в пункты выдачи.</p>

			<h4>Модель realFBS</h4>
			<p>Весь процесс хранения и доставки от начала до конца лежит в зоне ответственности продавца.</p>

			<h4>Модель FBO</h4>
			<p>Почти все задачи по фулфилменту выполняются маркетплейсом. После того, как продавец отгрузит товар на склад, он не участвует в процессе доставки.</p>

			<p>Если селлер выбрал FBO, отгружать товары он может одним из трёх способов:</p>
			<ul>
				<li><strong>Через виртуальный распределительный центр.</strong> Подходит тем, для кого расположение склада не критично, либо тем, кому важна скорость. Требуется оставить заявку на поставку, после чего внутренние алгоритмы Ozon оценят загруженность складов, находящихся поблизости, и предложат наиболее подходящий по дате, времени и расположению. В таких случаях магазин может отправить курьера, чтобы он оформил передачу партии товаров и забрал её.</li>
				<li><strong>На лично выбранный склад.</strong> Как это делается, было описано выше. Оптимально, когда есть время доставить самостоятельно, не требуется торопиться и выбирать дальние склады.</li>
				<li><strong>На лично выбранный склад через кросс-докинг.</strong> Особый вид услуги от Озон, позволяющий ускорить доставку в дальние города. За счёт кросс-докинга можно значительно расширить географию продаж.</li>
			</ul>

			<p>Ни один из этих способов не хуже и не лучше другого. Продавец выбирает, исходя из текущей ситуации и собственного комфорта.</p>

			<h3>Добавление заказа в план</h3>
			<p>Как только товары отмечены галочками, опция создания заявки будет разблокирована. Для селлера откроется форма для заполнения. В ней, помимо прочего, указаны даты и время для свободных слотов — это «окна», когда селлер может привезти свои товары. Они отображаются без фона, в то время как занятые отмечены серым цветом.</p>

			<p>После выбора подходящего времени нужно следовать предложенной пошаговой схеме на сайте. Продавец заполняет все поля с информацией о товаре, затем об автомобиле, на котором он будет доставлен, и о водителе. После этого ему сгенерируют пропуск.</p>

			<h3>Генерация штрихкодов</h3>
			<p>На всю продукцию маркетплейса штрихкоды обязательны к оформлению. Здесь есть два варианта на выбор:</p>
			<ul>
				<li><strong>Готовые данные от изготовителя.</strong> Подходит тем, кто перепродаёт товары чужого производства.</li>
				<li><strong>Сгенерированные штрихкоды с сайта Озон.</strong> Подходят всем.</li>
			</ul>

			<p>Этикетка со штрихкодом тоже строго стандартизирована. Она должна соответствовать базовым требованиям:</p>
			<ul>
				<li>размер этикетки — 12*7,5 см;</li>
				<li>используемая бумага может быть только матовой или полуглянцевой, а глянцевая не допускается;</li>
				<li>этикетка должна быть приклеена с той стороны коробки, которая больше других;</li>
				<li>печать выполнена термопринтером.</li>
			</ul>

			<p>Индивидуальная упаковка требуется для каждой единицы товара. Если продукция не относится к хрупкой, то её пакуют в полиэтилен и перематывают стрейч-плёнкой. И уже после этого, поверх всего, приклеивается штрих-код.</p>

			<p>Также не стоит забывать о базовой этикетке на каждом товаре. Если продукция чужого производства, достаточно просто проверить данные за изготовителем. Если собственного, то нужно подготовить всю информацию и разместить её на этикетках. Сюда входит дата производства, срок годности, название, артикул и наименование производителя, состав и основные технические характеристики.</p>

			<h3>Заполнение сведений об автомобиле и водителе</h3>
			<p>Как правило, селлеры не занимаются доставкой самостоятельно. В зависимости от масштабов продаж они либо вызывают курьеров, либо пользуются услугами грузоперевозок, либо нанимают сотрудника для этой цели. Но на склад маркетплейса, а также на прилегающую территорию, для посторонних входа нет. Поэтому водителю нужно будет оформить пропуск.</p>

			<p>Но до того, как это будет сделано, потребуется внести информацию о нём и о том транспорте, на котором он будет осуществлять поставку товаров.</p>

			<p>Делается это в личном кабинете, поэтапно:</p>
			<ul>
				<li>В разделе заявок селлер выбирает название склада.</li>
				<li>Затем требуется нажать на «Грузовые места».</li>
				<li>Внести данные о количестве мест. Это число паллет и коробок.</li>
				<li>Указать дату, в которую будет доставлен товар.</li>
				<li>Заполнить данные об отправителе, то есть о своём ИП или компании.</li>
				<li>Далее вносится информация о водителе. Здесь понадобится указать должность и ФИО.</li>
				<li>Также вписывается номер автомобиля, на котором он доставит товары.</li>
			</ul>

			<p>У крупных продавцов на Озон продукция зачастую ввозится крупными партиями, и для этого используется несколько машин сразу. В таких случаях в личном кабинете нужно дополнительно выбрать пункт «Отгрузка со склада», перейти в меню внутри этого подраздела и там указать списком все транспортные средства и информацию о водителях к каждому из них.</p>

			<h3>Формирование информационной таблицы</h3>
			<p>Для загрузки полной информации о товаре предлагается использовать онлайн-таблицы. Хранение данных в таком виде позволяет быстро проверить:</p>
			<ul>
				<li>название;</li>
				<li>цену;</li>
				<li>точность ссылок;</li>
				<li>технические характеристики;</li>
				<li>фотографии, цвета и оттенки;</li>
				<li>чёткость изображений, соответствие их размеров и разрешения требованиям маркетплейса.</li>
			</ul>

			<p>Таблицу можно создать по пошаговому плану, приведённому ниже:</p>
			<ol>
				<li>Открыть раздел таблиц в личном кабинете.</li>
				<li>Загрузить выбранные позиции, используя предложенный шаблон площадки.</li>
				<li>Указать категорию для каждого товара.</li>
				<li>Заполнить все поля, где требуется указать характеристики — цвет, размер и другие.</li>
				<li>Отослать таблицу модераторам для проверки.</li>
				<li>Если найдутся недочёты, исправить документ в соответствии с замечаниями.</li>
			</ol>

			<h3>Оформление пропуска</h3>
			<p>После того как будут внесены все данные о товаре и машине, а также о водителе, пропуск в личном кабинете появится автоматически. Его необходимо скачать и распечатать. Водители предъявляют его в бумажном виде. Пропуск понадобится при въезде на территорию, прилегающую к складу Ozon.</p>

			<p>Особые требования касаются тех, кто привозит продукцию на паллетах. В этом случае автомобиль должен соответствовать прописанным правилам:</p>
			<ul>
				<li>задний способ загрузки;</li>
				<li>ширина проёма для выгрузки не менее 2 метров;</li>
				<li>высота расположения заднего борта — не менее 80 см от земли;</li>
				<li>покрытие пола, позволяющее использовать погрузчик.</li>
			</ul>

			<h2>Отгрузка товара на склад</h2>
			<p>На складе есть контроль качества. Сотрудники проверяют привезённый товар по ряду критериев:</p>
			<ul>
				<li>срок годности;</li>
				<li>наличие повреждений;</li>
				<li>соответствие заявленного количества фактическому;</li>
				<li>наличие этикеток и их состояние — как на упаковках, так и на коробках;</li>
				<li>штрихкоды;</li>
				<li>соответствие артикулов.</li>
			</ul>

			<p>Обычно продавец предъявляет документы, сдаёт товар, дожидается базовой поверхностной проверки, после чего уезжает. Полное сравнение проводится сотрудниками в течение двух суток. Если обнаружены нарушения, селлера обяжут заменить продукцию или забрать её.</p>

			<p>Информация о том, успешно ли товар прошёл проверку, отобразится в личном кабинете. Статус можно отслеживать в разделе по продажам, перейдя в блок заявок, а затем во вкладку «Подтверждённые».</p>

			<p><strong>ВАЖНО!</strong> После одобрения сортировка занимает ещё до 3 дней.</p>

			<h2>Основные ошибки при поставке товара на Ozon</h2>
			<p>Беспечное отношение к поставкам на склад приводит к тому, что продавец теряет время и деньги, поскольку ему отказывают в приёмке. Несмотря на то, что Ozon предоставляет максимум полезной информации, не каждый готов подробно её изучать.</p>

			<p>Ниже собраны самые часто встречающиеся ошибки, из-за которых могут не принять товар:</p>
			<ul>
				<li><strong>Попытка сдать негабарит.</strong> Озон не занимается доставкой такой продукции. Для её отправки нужно использовать схему realFBS.</li>
				<li><strong>Несоответствие данных заявки и накладной.</strong> В таких случаях чаще всего имеют место банальные опечатки или неправильно посчитанный перед отправкой на склад товар.</li>
				<li><strong>Ошибки в расчёте цен и комиссий.</strong> Здесь может сыграть роль невнимательность.</li>
				<li><strong>Неправильно указанное количество грузомест.</strong> Под грузоместами подразумевается число коробок или других единиц упаковки.</li>
				<li><strong>Нет штрихкодов, этикеток или ярлыков.</strong> Это базовое требование Ozon, которое позволяет не терять товары и доставлять покупателям именно то, что они заказывали.</li>
				<li><strong>Нет универсального передаточного документа в системе ЭДО.</strong> Обязательный пункт для посредников.</li>
				<li><strong>Есть повреждения, либо зафиксированы нарушения требований к сборке и упаковке.</strong> К этому вопросу маркетплейс тоже относится очень серьёзно, а потому все нюансы лучше изучить заранее.</li>
			</ul>

			<p>В случае выявления проблем данные о том, по какой причине товар не прошёл, отобразятся в личном кабинете. При этом полностью разъяснять ошибку не будут: ограничатся общими фразами. Например, если при поставке на склад выявили неправильную упаковку, уточнять, что именно было не так, не станут.</p>

			<h2>Итоги</h2>
			<p>Подводя итог, можно сказать, что нужно соблюдать все правила отправки товаров на склад или в пункт выдачи. Но процесс перестаёт быть сложным после нескольких доставок. А потому данному вопросу нужно просто уделить время.</p>
		</BlogPost>
	)
}
