import { BlogPost } from '@/modules/blogPost'
import { getPosts } from '@/app/blog/posts'
import type { Metadata } from 'next'
// eslint-disable-next-line @typescript-eslint/consistent-type-imports
import meta from './meta.json'

export const metadata: Metadata = {
	title: meta.title,
	description: meta.description ?? ''
}

export default async function PostPage() {
	const currentId = 'skolko-stoit-xranenie-tovara-na-sklade-ozon'
	const posts = await getPosts()
	const related = posts
		.filter(p => p.id !== currentId)
		.map(p => ({
			id: p.id,
			title: p.title,
			href: `/blog/${p.id}`,
			description: p.description,
			imageSrc: p.imageSrc,
			date: p.date,
			tags: p.tags
		}))

	return (
		<BlogPost
			title="Сколько стоит хранение товара на складе Ozon"
			author="Александр Петров"
			authorRole="Учредитель компании"
			date="11.09.2024"
			readTimeMin={5}
			views={4321}
			imageSrc="/images/banner.jpg"
			currentId={currentId}
			related={related}
		>
			<p>Озон заинтересован в том, чтобы селлеры хранили товары по схеме FBO, то есть поддерживали внутреннюю логистику маркетплейса. Причина не только в том, что площадке это выгодно. Хранение товара на складе Ozon позволяет уменьшить количество форс-мажоров, из-за которых покупатель может остаться без своего заказа или получить его с опозданием. Чтобы мотивировать селлеров, маркетплейс, помимо прочего, поощряет их так называемыми литробонусами — внутренней «валютой», которая, фактически, даёт возможность снизить оплату за хранение товара на складе площадки.</p>

			<h2>Формула расчета за размещение на складе Озон</h2>
			<p>Стоимость хранения по схеме FBO высчитывается по такой формуле:</p>
			<p><strong>Плата за размещение = Коэффициент литробонусов × Объём × Срок</strong></p>

			<p>Озон считает объём в литрах. Если нужно замерить заранее, нужно ориентироваться на то, сколько квадратных дециметров вы планируете привезти:</p>
			<ul>
				<li>1 литр = 1 дм²</li>
				<li>1 дм² = куб 10×10×10 см</li>
				<li>либо коробка, у которой высота × ширина × длина = 1000 см.</li>
			</ul>

			<p>Следовательно, основная цена складывается из того, сколько места занимает товар и на какой срок его привезли. Очевидно, что время хранения с самого начала невозможно определить, поэтому конечная стоимость плавающая — продавец знает лишь то, сколько он заплатит за каждый кубометр. В то же время наличие литробонусов позволяет рассчитывать на хорошие скидки.</p>

			<p><strong>ВАЖНО!</strong> Первые 60 дней после привоза за хранение товаров деньги не взимаются. Это касается размещения новых позиций, которые селлер ранее не продавал.</p>

			<p>Поскольку продажи идут постоянно, количество товара на складе будет снижаться. Следовательно, цифры первого дня через неделю уже не актуальны — общая сумма платежей за хранение уменьшится. При новой отгрузке она снова увеличится. Поэтому Озон делает расчёты каждые сутки.</p>

			<h2>Тарифы размещения по разным категориям</h2>
			<p>Фиксированной и постоянной разницы в оплате по категориям у Озона нет. Но есть некоторые особенности, из-за которых хранение товаров одной ниши может обходиться дороже или дешевле других:</p>
			<ul>
				<li>Для некоторых позиций предусмотрено поощрение от маркетплейса — бесплатное размещение. Подробнее об этом будет сказано далее.</li>
				<li>Существует понятие оборачиваемости — приблизительного срока, в течение которого товар продаётся в той или иной категории. Конечно, это усреднённый показатель по результатам всех селлеров ниши. Например, если оборачиваемость 60-90 дней, то продавцу это обойдётся бесплатно или намного дешевле, чем если он будет поставлять товары, у которых тот же показатель равен 200-250 дням.</li>
				<li>Некоторые категории попадают под особые условия. Они, в свою очередь, могут быть более выгодны для продавца или, наоборот, менее выгодны.</li>
			</ul>

			<p>Информация о категориях с особыми условиями размещается на официальном сайте в разделе «Расходы на другие услуги». Обычно это своеобразные скидки, действующие несколько месяцев в году. Часто они привязаны к сезону. Например, можно получить возможность не платить за размещение товаров с середины июня по середину сентября, если вы продаёте учебники.</p>

			<h2>Что такое литробонусы Ozon?</h2>
			<p>Литробонусы начисляются продавцу каждый раз, когда он доставляет партию товара на склад. Потратить их можно только на ту же позицию, за которую они были подарены.</p>

			<p>Озон заявляет, что каждый литробонус покрывает стоимость размещения товаров, а точнее одного литра за один день. Фактически подсчёт происходит проще: по формуле, которая была размещена выше. Литробонусы переводятся маркетплейсом в коэффициент. И чем он ниже, тем меньше в итоге придётся платить.</p>

			<p><strong>ВАЖНО!</strong> Литробонусы суммируются и сохраняются даже в период нулевого остатка на складе Ozon.</p>

			<h3>Как посчитать, сколько литробонусов начислит Озон?</h3>
			<p>Ozon предлагает использовать такую формулу:</p>
			<p><strong>Объём поставки × Коэффициент = Полученные литробонусы</strong></p>

			<p>При этом коэффициент рассчитывает сама площадка. Он различается для разных категорий и сильно зависит от оборачиваемости. Есть исключение для ряда городов и некоторых сортировочных центров. Также коэффициент может меняться со временем. Тарифы размещаются на сайте маркетплейса, в разделе расходов на другие услуги.</p>

			<h2>Как долго можно хранить товары на складах Озон?</h2>
			<p>Срок, в течение которого может находиться товар на складе, не ограничен. Но чем дольше он там лежит, тем больше за это платит продавец.</p>

			<p>Пока есть литробонусы, плата за размещение товаров высчитывается на основе коэффициента. Когда они закончатся, площадка выявляет оборачиваемость позиции именно у этого продавца. Далее подсчёт ведётся по этому показателю:</p>
			<ul>
				<li>Если оборачиваемость товара до 160 дней, то плата не предусмотрена.</li>
				<li>Если этот показатель составляет 160-180 дней, начисляется 0,75 рублей за каждый литр ежедневно.</li>
				<li>Если больше 180 дней — 1,5 рубля за литр в день. Тот же тариф действует, если продажи на паузе уже 2 месяца, независимо от причины.</li>
			</ul>

			<p>Стоит обратить внимание, что товары с истёкшим сроком годности склад Ozon утилизирует.</p>

			<h2>Бесплатное хранение на складе Ozon</h2>
			<p>Выше уже было сказано, что первые два месяца товары хранятся бесплатно. Также действуют особые условия для некоторых городов за пределами РФ — иногда есть возможность размещать очень дёшево или совсем без внесения денег за хранение.</p>

			<p>Также Ozon поощряет размещение товара на складе, если на него есть высокий спрос. В этом случае можно рассчитывать на отсутствие платежей.</p>

			<p>Чтобы получать больше литробонусов, рекомендуется привозить большой объём товара за раз либо делать поставки в период сезонности — перед праздниками, в мае, в августе. Также стоит отслеживать программы лояльности и подключаться к ним.</p>
		</BlogPost>
	)
}
