			<p>Чем выше индекс, тем больше скидок получает селлер за логистику компании. Значение вычисляется следующим образом. Если товар продавца к клиенту поступает из зоны покрытия, то ИЛ увеличивается, а если из другого кластера, то показатель падает, чем дальше склад Озон, тем сильнее снижается индекс локализации.</p>

