			<p>При выборе данной схемы товар оформляется, хранится и отгружается в ПВЗ со склада Ozon. Такие заказы находятся в приоритете, что сокращает сроки доставки. Схема FBO считается самой удобной, так как всю работу на себя берет маркетплейс.</p>

