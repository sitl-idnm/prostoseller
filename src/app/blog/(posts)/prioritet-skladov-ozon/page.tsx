			<p>В список городов с центральными складами входят: Тверь, Петровское, Пушкино 1 и 2, Хоругвино. Региональные находятся в следующих городах: Екатеринбург, Самара, Ростов-на-Дону, Казань, Нижний Новгород, Санкт-Петербург.</p>

